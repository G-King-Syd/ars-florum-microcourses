const mongoose = require("mongoose");

const courseSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  instructor: {
    type: String,
    required: true
  },
  duration: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  image: {
    type: String
  },
  modules: {
    type: [String]
  },
  enrolments: {
    type: Number,
    default: 0
  }
});

module.exports = mongoose.model("Course", courseSchema);
